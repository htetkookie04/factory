export function makeT(lang) {
  const dict = STRINGS[lang] || STRINGS.en
  return (key) => dict[key] ?? STRINGS.en[key] ?? key
}

// Helper to pull the localized label from a token option object {en, my}.
export function label(option, lang) {
  if (!option) return ''
  return option[lang] ?? option.en
}
